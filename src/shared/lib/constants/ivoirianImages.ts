/**
 * Récupère des images aléatoires pour une propriété
 */
export function getPropertyImages(
  neighborhood: string,
  propertyType: string,
  count: number = 5
): string[] {
  const images: string[] = [];

  // Normaliser le nom du quartier
  const normalizedNeighborhood = neighborhood
    .toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '');

  // Chercher les images du quartier
  const neighborhoodKey = Object.keys(IVOIRIAN_PROPERTY_IMAGES).find((key) =>
    normalizedNeighborhood.includes(key.toLowerCase())
  );

  if (neighborhoodKey) {
    const neighborhoodImages =
      IVOIRIAN_PROPERTY_IMAGES[neighborhoodKey as keyof typeof IVOIRIAN_PROPERTY_IMAGES];
    images.push(...neighborhoodImages.slice(0, 2));
  }

  // Ajouter des images selon le type de propriété
  const typeKey = propertyType.toLowerCase();
  const typeImages = Object.keys(PROPERTY_TYPE_IMAGES).find(
    (key) => typeKey.includes(key) || key.includes(typeKey)
  );

  if (typeImages) {
    const propertyTypeImages =
      PROPERTY_TYPE_IMAGES[typeImages as keyof typeof PROPERTY_TYPE_IMAGES];
    images.push(...propertyTypeImages.slice(0, 2));
  }

  // Compléter avec des images d'intérieur
  images.push(INTERIOR_IMAGES.salon[0] ?? '');
  images.push(INTERIOR_IMAGES.cuisine[0] ?? '');
  images.push(INTERIOR_IMAGES.chambre[0] ?? '');

  // Retourner le nombre demandé d'images uniques
  const uniqueImages = [...new Set(images)];
  return uniqueImages.slice(0, count);
}

/**
 * Récupère une image de fond pour un quartier
 */
export function getNeighborhoodImage(neighborhood: string): string {
  return (
    NEIGHBORHOOD_IMAGES[neighborhood as keyof typeof NEIGHBORHOOD_IMAGES] ||
    NEIGHBORHOOD_IMAGES['Cocody']
  );
}

/**
 * Récupère une image principale selon le type de propriété
 */
export function getMainPropertyImage(propertyType: string): string {
  const typeKey = propertyType.toLowerCase();

  if (typeKey.includes('villa')) {
    return PROPERTY_TYPE_IMAGES.villa[0] ?? '';
  } else if (typeKey.includes('appartement')) {
    return PROPERTY_TYPE_IMAGES.appartement[0] ?? '';
  } else if (typeKey.includes('studio')) {
    return PROPERTY_TYPE_IMAGES.studio[0] ?? '';
  } else if (typeKey.includes('duplex')) {
    return PROPERTY_TYPE_IMAGES.duplex[0] ?? '';
  } else if (typeKey.includes('maison')) {
    return PROPERTY_TYPE_IMAGES.maison[0] ?? '';
  }

  // Par défaut, une image moderne
  return PROPERTY_TYPE_IMAGES.appartement[0] ?? '';
}

/**
 * Génère un placeholder avec une image aléatoire représentative
 */
export function getRandomIvoirianPropertyImage(): string {
  const allImages = Object.values(PROPERTY_TYPE_IMAGES).flat();
  return allImages[Math.floor(Math.random() * allImages.length)] ?? '';
}
